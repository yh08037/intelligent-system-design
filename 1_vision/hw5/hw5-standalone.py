def load_fashion_mnist(normalize=True, one_hot_label=False):
    '''
    original code is found at:
    https://github.com/zalandoresearch/fashion-mnist/blob/master/utils/mnist_reader.py
    '''
    import os
    import gzip


    key_list = [
        ('train_img', 'train_label'),
        ('test_img', 'test_label')
    ]

    key_file = {
        'train_img':'train-images-idx3-ubyte.gz',
        'train_label':'train-labels-idx1-ubyte.gz',
        'test_img':'t10k-images-idx3-ubyte.gz',
        'test_label':'t10k-labels-idx1-ubyte.gz'
    }

    dataset = {}

    # load data from gzip files
    for key_img, key_label in key_list:
        images_path = os.path.join(key_file[key_img])
        labels_path = os.path.join(key_file[key_label])

        with gzip.open(labels_path, 'rb') as lbpath:
            labels = np.frombuffer(lbpath.read(), dtype=np.uint8, offset=8)
        
        with gzip.open(images_path, 'rb') as imgpath:
            images = np.frombuffer(imgpath.read(), dtype=np.uint8,
                                offset=16).reshape(len(labels), 784)

        dataset[key_label] = labels
        dataset[key_img]   = images    

    # normalization of input data
    if normalize:
        for key in ('train_img', 'test_img'):
            dataset[key] = dataset[key].astype(np.float32)
            dataset[key] /= 255.0
    
    # one-hot encoding
    if one_hot_label:
        dataset['train_label'] = np.eye(10)[dataset['train_label']]
        dataset['test_label']  = np.eye(10)[dataset['test_label']]

    return (dataset['train_img'], dataset['train_label']), (dataset['test_img'], dataset['test_label']) 
